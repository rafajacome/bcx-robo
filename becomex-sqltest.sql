  INNER JOIN ITEM_PEDIDO IP
  ON PD.ID_PEDIDO = IP.ID_PEDIDO
  WHERE PD.DATA_PEDIDO >=  TO_DATE('01/01/2012', 'DD/MM/YYYY') and PD.DATA_PEDIDO <= TO_DATE('31/12/2012', 'DD/MM/YYYY')
